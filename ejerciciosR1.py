# Ejercicio 9

#Declara variables de cada uno de los tipos: int, float, str y bool.
varriable_int = 10
variable_float = 0.5
variable_str = "El Salvador"
variable_bool = True

# Verifica su tipo con type().
# No se puede hacer una concatenacion de Type(). porque son objetos y en Python no se pueden sumar objetos type con +
# Por eso he separado los print(type()) en diferentes print() y separados por comas ","
print(type(varriable_int)), print(type(variable_float)), print(type(variable_str)), print(type(variable_bool))

